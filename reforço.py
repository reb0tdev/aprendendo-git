a = int(input ('Digite um Número:'))
b = int(input ('Digite Outro Número:'))

soma = a+b
multiplicação = a*b
divisão = a/b
print (f'A soma de: {a}+{b} = {soma}') 
print (f'A multiplicação de: {a}*{b} = {multiplicação}')
print (f'A Divisão de: {a}/{b} = {divisão} ')


